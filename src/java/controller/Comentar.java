    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            String comentario = request.getParameter("comentario");
            String usuario = request.getParameter("nickname");
            String publicacion = request.getParameter("publicacion");
            int ID=Basedatos.retornarUsuarioId(usuario);
            int IDpublicacion=Integer.parseInt(publicacion);
            if(Basedatos.comentar(ID, IDpublicacion, comentario)){
                        request.setAttribute("mensaje", "Se envio el comentario");
            }
            else
                        request.setAttribute("mensaje", "No se pudo enviar el comentario");
            
            

        request.getRequestDispatcher("comentar.jsp").forward(request, response);
    }

